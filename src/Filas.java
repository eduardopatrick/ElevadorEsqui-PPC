public class Filas {

    final int numeroDeFilas = 4;
    private int[] esquerdaSozinho;
    private int[] esquerdaTripla;
    private int[] direitaTripla;
    private int[] direitaSozinho;


    public int getNumeroDeFilas() {
        return numeroDeFilas;
    }

    public int[] getEsquerdaSozinho() {
        return esquerdaSozinho;
    }

    public void setEsquerdaSozinho(int[] esquerdaSozinho) {
        this.esquerdaSozinho = esquerdaSozinho;
    }

    public int[] getEsquerdaTripla() {
        return esquerdaTripla;
    }

    public void setEsquerdaTripla(int[] esquerdaTripla) {
        this.esquerdaTripla = esquerdaTripla;
    }

    public int[] getDireitaTripla() {
        return direitaTripla;
    }

    public void setDireitaTripla(int[] direitaTripla) {
        this.direitaTripla = direitaTripla;
    }

    public int[] getDireitaSozinho() {
        return direitaSozinho;
    }

    public void setDireitaSozinho(int[] direitaSozinho) {
        this.direitaSozinho = direitaSozinho;
    }
}
